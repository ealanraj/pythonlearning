##ARITHMETIC OPERATORS
##1) area of the square:
a=int(input("enter the value of square area:"))
area=a**2
print("area of the square:",area)
##2) Area of the Rectangle:
l=int(input("enter the rectangle length:"))
b=int(input("enter the rectangle breadth:"))
area=l*b
print("area of the rectangle:",area)
##3) Area of the cube:
a=int(input("enter the cube area:"))
area=a**3
print("area of the cube:",area)
##4) Area of the circle:
r=int(input("enter the circle radius:"))
pi=3.14
area=pi*r**2
print("area of the circle:",area)
##5) Simple Interest:
p=int(input("enter the principal value:"))
n=int(input("enter the no.of years:"))
r=int(input("enter the rate of interest:"))
simple_interest=p*n*r/100
print("simple interest:",simple_interest)
