##Identity Operator

##(i)immutable Datatype:

a=19
b=19
print(id(a))
print(id(b))
print(a is b)
print(a is not b)
print()

##(ii)mutable datatype
a={1,2,3,4}
b={1,2,3,4}
print(id(a))
print(id(b))
print(a is b)
print(a is not b)
